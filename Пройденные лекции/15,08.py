# Задача №8 (случайный раздел).Раздел 9: ООП (Классы)
# Задача:
# Создай класс Book с атрибутом title (передаётся при создании объекта) и методом show_title(),
# который выводит "Название: {title}".

class Book:
    def __init__(self, title):
        self.title = title

    def show_title(self):
        return f"Название:'{self.title}'"


book = Book("jfhfh")
print(book.show_title())

# Задача №9 (случайный раздел).Раздел 10: Наследование
# Задача:
# Создай родительский класс Vehicle с методом move() (пусть возвращает "Транспорт движется"), затем дочерний класс Car,
# который переопределяет этот метод (возвращает "Автомобиль едет").
class Vehicle:
    def move(self):
        return "Транспорт движется"


class Car(Vehicle):
    def move(self):
        return "Автомобиль едет"

car = Car()
print(car.move())

